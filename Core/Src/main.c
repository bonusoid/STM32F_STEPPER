/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  unsigned char sti; //Step increment
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	  if((HAL_GPIO_ReadPin(BTNIO, BTNCW))==GPIO_PIN_RESET) //If CW Button is pressed (Active Low)
	  {
	  		for(sti=0;sti<4;sti++)
	  		{
	  			setstep(stepcw[sti]); //Run CW sequence
	  			HAL_Delay(10);
	  		}
	  }
	  else if((HAL_GPIO_ReadPin(BTNIO, BTNCCW))==GPIO_PIN_RESET) //If CCW Button is pressed (Active Low)
	  {
	  		for(sti=0;sti<4;sti++)
	  		{
	  			setstep(stepccw[sti]); //Run CCW sequence
	  			HAL_Delay(10);
	  		}
	  }
	  else
	  {
	  		MOTIO->ODR = 0x00;
	  }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

